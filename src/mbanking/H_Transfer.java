    private void btn_okMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_okMouseClicked
        // TODO add your handling code here:
        
        int saldotf = Integer.parseInt(txt_saldotf.getText());
        
        if(txt_rektj.getText() == ""){
            JOptionPane.showMessageDialog(null, "NO REK TUJUAN TIDAK BOLEH KOSONG");
        }else if(txt_saldotf.getText() == ""){
            JOptionPane.showMessageDialog(null, "SALDO TIDAK BOLEH KOSONG");
        }else if(saldotf <= 10000){
            JOptionPane.showMessageDialog(null, "MINIMUN TRANSFER 10.000");
        }else if(txt_norek1.getText() == txt_rektj.getText()){
            JOptionPane.showMessageDialog(null, "NOMOR REKENING TUJUAN TIDAK BOLEH SAMA DENGAN REKENING ANDA");
        }else{
            txt_pintf.setVisible(true);
            label_input_pin.setVisible(true);
            btn_tf.setVisible(true);
            btn_canceltf.setVisible(true);
        }
    }//GEN-LAST:event_btn_okMouseClicked

