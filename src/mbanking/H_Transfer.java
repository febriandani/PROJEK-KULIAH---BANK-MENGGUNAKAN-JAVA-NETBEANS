    private void btn_tfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tfActionPerformed
        // TODO add your handling code here:
        Date current = new Date();
        SimpleDateFormat frmttgl = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = frmttgl.format(current);
        conn = koneksi.getkoneksi();
        String sql1 = "SELECT * FROM nasabah WHERE `nomor_rekening` LIKE '%"+txt_rektj.getText()+"%'";
        try{
            st = (Statement) conn.createStatement();
            rs = st.executeQuery(sql1);
            
            int pintf = Integer.parseInt(txt_pintf.getText());
            
            if(rs.next() && pintf == rs.getInt(11)){
                String saldo_awal = rs.getString("saldo");
                int saldo_awal1 = Integer.parseInt(saldo_awal);
                int saldo_kirim = Integer.parseInt(txt_saldotf.getText());
                int saldo_terbaru = saldo_awal1  + saldo_kirim;
                String nama = rs.getString("nama_lengkap");
                String ket = "Transfer ke " + nama;
                Integer.toString(saldo_terbaru);
                
                st.executeUpdate("UPDATE nasabah SET saldo = '"+saldo_terbaru+"' WHERE nomor_rekening = '"+txt_rektj.getText()+"'");
                st.executeUpdate("UPDATE login_akses SET saldo_awal = '"+saldo_terbaru+"' WHERE nomor_rekening = '"+txt_rektj.getText()+"'");
                kurang_saldo();
                JOptionPane.showMessageDialog(null, "TRANSFER BERHASIL");
            
                    H_InputKodeAkses H_IKA = new H_InputKodeAkses();
                    H_IKA.setVisible(true);
                    this.getDesktopPane().add(H_IKA);
                    this.dispose();
                    cls();
                    st.execute("INSERT INTO `histori` (`dari_rekening`, `tanggal`, `keterangan`, `jumlah`) VALUES ('"+txt_norek1.getText()+"','"+ tanggal +"','"+ ket +"','"+ saldo_kirim +"')"); 
            }else{
                JOptionPane.showMessageDialog(null, "PIN SALAH!");
            }
         
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "" + e);
        }
    }//GEN-LAST:event_btn_tfActionPerformed

