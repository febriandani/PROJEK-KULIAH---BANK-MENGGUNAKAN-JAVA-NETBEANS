    public void kurang_saldo(){
        conn = koneksi.getkoneksi();
         String sql1 = "SELECT * FROM nasabah WHERE `nomor_rekening` LIKE '%"+txt_norek1.getText()+"%'";
         try{
         st = (Statement) conn.createStatement();
            rs = st.executeQuery(sql1);
            if(rs.next()){
                String saldo_awal = rs.getString("saldo");
                int saldo_awal1 = Integer.parseInt(saldo_awal);
                int saldo_kirim = Integer.parseInt(txt_saldotf.getText());
                int saldo_terbaru = saldo_awal1  - saldo_kirim;
                Integer.toString(saldo_terbaru);
//                SELECT harga,harga + 50 FROM penjualan WHERE harga=11000;
                st.executeUpdate("UPDATE nasabah SET saldo = '"+saldo_terbaru+"' WHERE nomor_rekening = '"+txt_norek1.getText()+"'");
                st.executeUpdate("UPDATE login_akses SET saldo_awal = '"+saldo_terbaru+"' WHERE nomor_rekening = '"+txt_norek1.getText()+"'");
                JOptionPane.showMessageDialog(null, "TRANSFER BERHASIL");
                cls();
                
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "" + e);
        }
        
    }
    
